package day02;

public class C2_EscapaSquences {

    public static void main(String[] args){

        System.out.println(" Renas Tech");

        // \t text ---- this will give an extra space in your code

        System.out.println("\t Renas Tech");
        System.out.println("\t\t\t\t Renas Tech");


        // \n means new line -- text will be displayed on new line

        System.out.println("Hello World");
        System.out.println("Hello \n World");
        System.out.println("Hello\n\t World");


        // \\ --this will print single slash on the \ consul

        System.out.println("\\");

        System.out.println("/");

        //you have text inside text you want to use "" :
        System.out.println("Today is \" Saturday\" ");

        // to have a single quote
        System.out.println("Today is \' Saturday\' ");
        System.out.println("Today is ' Saturday' ");

        String txt="hello everybody";
        System.out.println(txt.indexOf("y"));

        int i=1;
        while (1<=6){
            System.out.println(i);
            i++;
        }


    }
}
