package day02;

public class C1_printVSprintln {

    public static void main(String [] args){
        System.out.println("This is println"); //print line breaks the line
        //second line
        System.out.print("--This is print"); //print never breaks the line it will continue
        // second line
        System.out.print("-- continue print");
        //second line
        System.out.println("   println 2");

        //after print line will move to 3. line
        // third line
        System.out.print("   print 2");

        System.out.print("print 3");
        // this line wont break the line


    }

}
