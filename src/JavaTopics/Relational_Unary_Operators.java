package JavaTopics;

public class Relational_Unary_Operators {

    public static void main(String[] args) {

        //Relational operators

        int a=20;
        int b=25;

        boolean exe= a==b;

        //== equal operator

        System.out.println(a==b);  // 20==25---> false


        // != not equal operator
        System.out.println(a!=b);  //20!=25-----> true


        // > grater than operator
        System.out.println(a>b); //20>25 ----> false

        // < less than operator
        System.out.println(a<b);  //20<25 ----->true

        // >= grater than or equal operator
        System.out.println(a>=b); //20>=25 ---->false

        // <= less than or equal operator
        System.out.println(a<=b);  //20<=25 ---->true
    }
}
